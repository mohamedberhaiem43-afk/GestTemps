    ALTER TABLE employe ADD empsbase_tmp NVARCHAR(256) NULL;
    EXEC('UPDATE employe SET empsbase_tmp = CAST(empsbase AS NVARCHAR(256)) WHERE empsbase IS NOT NULL');
