-- ============================================================
-- RGPD Compliance Migration Script
-- Adds CreatedAt, DeletedAt, RetentionDate columns to all tables
-- Creates AuditLog table for RGPD audit trail
-- ============================================================

-- 1. Create AuditLog table (with RGPD compliance fields)
IF NOT EXISTS (SELECT * FROM INFORMATION_SCHEMA.TABLES WHERE TABLE_NAME = 'AuditLog')
BEGIN
    CREATE TABLE AuditLog (
        Id INT IDENTITY(1,1) PRIMARY KEY,
        Uticod NVARCHAR(20) NULL,
        Action NVARCHAR(100) NULL,
        TableName NVARCHAR(100) NULL,
        DateAction DATETIME NOT NULL DEFAULT GETUTCDATE(),
        CreatedAt DATETIME NULL,
        DeletedAt DATETIME NULL,
        RetentionDate DATETIME NULL
    );
END;
GO

-- 1b. If AuditLog table already exists, migrate columns
IF EXISTS (SELECT * FROM INFORMATION_SCHEMA.TABLES WHERE TABLE_NAME = 'AuditLog')
BEGIN
    -- Add Uticod column if not exists
    IF NOT EXISTS (SELECT * FROM INFORMATION_SCHEMA.COLUMNS WHERE TABLE_NAME = 'AuditLog' AND COLUMN_NAME = 'Uticod')
        ALTER TABLE AuditLog ADD Uticod NVARCHAR(20) NULL;

    -- Add RGPD columns if not exists
    IF NOT EXISTS (SELECT * FROM INFORMATION_SCHEMA.COLUMNS WHERE TABLE_NAME = 'AuditLog' AND COLUMN_NAME = 'CreatedAt')
        ALTER TABLE AuditLog ADD CreatedAt DATETIME NULL;
    IF NOT EXISTS (SELECT * FROM INFORMATION_SCHEMA.COLUMNS WHERE TABLE_NAME = 'AuditLog' AND COLUMN_NAME = 'DeletedAt')
        ALTER TABLE AuditLog ADD DeletedAt DATETIME NULL;
    IF NOT EXISTS (SELECT * FROM INFORMATION_SCHEMA.COLUMNS WHERE TABLE_NAME = 'AuditLog' AND COLUMN_NAME = 'RetentionDate')
        ALTER TABLE AuditLog ADD RetentionDate DATETIME NULL;

    -- Drop old columns if they exist
    IF EXISTS (SELECT * FROM INFORMATION_SCHEMA.COLUMNS WHERE TABLE_NAME = 'AuditLog' AND COLUMN_NAME = 'UserId')
        ALTER TABLE AuditLog DROP COLUMN UserId;
    IF EXISTS (SELECT * FROM INFORMATION_SCHEMA.COLUMNS WHERE TABLE_NAME = 'AuditLog' AND COLUMN_NAME = 'RecordId')
        ALTER TABLE AuditLog DROP COLUMN RecordId;
    IF EXISTS (SELECT * FROM INFORMATION_SCHEMA.COLUMNS WHERE TABLE_NAME = 'AuditLog' AND COLUMN_NAME = 'user_id')
        ALTER TABLE AuditLog DROP COLUMN user_id;
    IF EXISTS (SELECT * FROM INFORMATION_SCHEMA.COLUMNS WHERE TABLE_NAME = 'AuditLog' AND COLUMN_NAME = 'record_id')
        ALTER TABLE AuditLog DROP COLUMN record_id;
END;
GO

